  }

  Future<String> exportReceiptPdf({
    required String businessName,
    required String businessPhone,
    required String businessAddress,
    required String invoiceNo,
    required SaleDraft sale,
    required DateTime soldAt,
    String? customerName,
  }) async {
    final bytes = await buildReceiptPdf(
      pageFormat: PdfPageFormat.roll80,
      businessName: businessName,
      businessPhone: businessPhone,
      businessAddress: businessAddress,
      invoiceNo: invoiceNo,
      sale: sale,
      soldAt: soldAt,
      customerName: customerName,
    );
    final safeInvoice = invoiceNo.replaceAll(RegExp(r'[^A-Za-z0-9_-]'), '-');
    final path = await _exportPath('receipt-$safeInvoice', 'pdf');
    await File(path).writeAsBytes(bytes, flush: true);
    return path;
  }

  static pw.Widget _sectionTitle(String value) {
    return pw.Text(
      value,
      style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
    );
  }

  static pw.Widget _emptySection(String message) {
    return pw.Container(
      width: double.infinity,
      padding: const pw.EdgeInsets.all(12),
      decoration: pw.BoxDecoration(
        color: PdfColors.grey100,
        border: pw.Border.all(color: PdfColors.grey300, width: 0.5),
      ),
      child: pw.Text(message, style: const pw.TextStyle(fontSize: 9)),
    );
  }

  static pw.Widget _metricBox(String label, String value) {
    return pw.Container(
      width: 160,
      padding: const pw.EdgeInsets.all(12),
      decoration: pw.BoxDecoration(
        border: pw.Border.all(color: PdfColors.blueGrey200),
        borderRadius: pw.BorderRadius.circular(6),
      ),
      child: pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          pw.Text(
            label,
            style: const pw.TextStyle(fontSize: 9, color: PdfColors.grey700),
          ),
          pw.SizedBox(height: 6),
          pw.Text(
            value,
            style: pw.TextStyle(fontSize: 14, fontWeight: pw.FontWeight.bold),
          ),
        ],
      ),
    );
  }

  static pw.Widget _receiptRule() {
    return pw.Container(
      height: 0.7,
      width: double.infinity,
      color: PdfColors.black,
    );
  }

  static pw.Widget _receiptDetail(String label, String value) {
    return pw.Padding(
      padding: const pw.EdgeInsets.only(bottom: 2),
      child: pw.Row(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          pw.SizedBox(
            width: 46,
            child: pw.Text(
              '$label:',
              style: pw.TextStyle(fontSize: 8, fontWeight: pw.FontWeight.bold),
            ),
          ),
          pw.Expanded(
            child: pw.Text(value, style: const pw.TextStyle(fontSize: 8)),
          ),
        ],
      ),
    );
  }

  static pw.Widget _receiptTotal(
    String label,
    String value, {
    bool emphasized = false,
  }) {
    final style = pw.TextStyle(
      fontSize: emphasized ? 12 : 9,
      fontWeight: emphasized ? pw.FontWeight.bold : pw.FontWeight.normal,
    );
    return pw.Padding(
      padding: const pw.EdgeInsets.only(bottom: 2),
      child: pw.Row(
        mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
        children: [
          pw.Text(label, style: style),
          pw.Text(value, style: style),
        ],
      ),
    );
  }

  Future<String> _exportPath(String prefix, String extension) async {
    final documents = await getApplicationDocumentsDirectory();
    final directory = Directory(
      p.join(documents.path, 'Airmonlink Business Manager', 'Exports'),
    );
    await directory.create(recursive: true);
    final timestamp = DateTime.now()
        .toIso8601String()
        .replaceAll(':', '-')
        .replaceAll('.', '-');
    return p.join(directory.path, '$prefix-$timestamp.$extension');
  }

  static PdfPageFormat _safeReceiptFormat(
    PdfPageFormat requested,
    int itemCount,
  ) {
    final requestedWidth = requested.width.isFinite && requested.width > 0
        ? requested.width
        : PdfPageFormat.roll80.width;

    final width = requestedWidth <= 65 * PdfPageFormat.mm
        ? 57 * PdfPageFormat.mm
        : 80 * PdfPageFormat.mm;

    final estimatedHeight =
        (150 + itemCount.clamp(1, 100) * 16) * PdfPageFormat.mm;

    final height = estimatedHeight.clamp(
      180 * PdfPageFormat.mm,
      1000 * PdfPageFormat.mm,
    );

    return PdfPageFormat(width, height.toDouble());
  }

  static PdfPageFormat _safePageFormat(
    PdfPageFormat requested,
    PdfPageFormat fallback,
  ) {
    if (!requested.width.isFinite ||
        !requested.height.isFinite ||
        requested.width <= 0 ||
        requested.height <= 0) {
      return fallback;
    }
    return requested;
  }

  static Uint8List _validatedPdfBytes(Uint8List bytes, String documentName) {
    final header = bytes.length >= 5 ? String.fromCharCodes(bytes.take(5)) : '';
    if (bytes.length < 500 || header != '%PDF-') {
      throw StateError(
        'The $documentName PDF could not be generated correctly.',
      );
    }
    return bytes;
  }

  static String _csvCell(String value) {
    final safeValue = RegExp(r'^[=+\-@]').hasMatch(value) ? "'$value" : value;
    final escaped = safeValue.replaceAll('"', '""');
    return '"$escaped"';
  }

  static String _pdfMoney(num value) => 'GHS ${value.toStringAsFixed(2)}';

  static String _quantity(double value) {
    if (value == value.roundToDouble()) return value.toStringAsFixed(0);
    return value.toStringAsFixed(2);
  }
}
